SELECT 

	IT.ITEMID AS "Codigo", 
	ERPT.NAME AS "Descricao", 
	ITM.UNITID AS "Unidade_Venda", 
	0 AS "Preco1",
	0 AS "Preco2",
	IT.NETWEIGHT AS "Peso_Liquido"

FROM ECORESPRODUCT ERP
	JOIN ECORESPRODUCTTRANSLATION ERPT ON ERPT.PRODUCT = ERP.RECID AND ERPT.LANGUAGEID = 'PT-BR'
	JOIN INVENTTABLE IT ON IT.PRODUCT = ERP.RECID
	JOIN INVENTTABLEMODULE ITM ON ITM.ITEMID = IT.ITEMID AND ITM.DATAAREAID = IT.DATAAREAID AND ITM.MODULETYPE = 2 -- Venda

WHERE IT.DATAAREAID = 'OMT' AND IT.COSTGROUPID IN ('040', '110', '130', '170')
